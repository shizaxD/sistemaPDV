    private void modificamenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificamenuActionPerformed
        int fila = jTable1.getSelectedRow();
        int columna = jTable1.getSelectedColumn();
        columnam = jTable1.getColumnName(columna);
        if (fila >= 0) {
            txtcodigo.setText(jTable1.getValueAt(fila, 0).toString());
            txtcampo.setText(jTable1.getValueAt(fila, columna).toString());
            id = jTable1.getValueAt(fila, 0).toString();

        } else {
            JOptionPane.showMessageDialog(null, "No se encontro fila");
        }

    }//GEN-LAST:event_modificamenuActionPerformed

