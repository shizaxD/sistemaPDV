    void CentrarHijas(JInternalFrame hija) {
        int ancho = (panel.getWidth() / 2) - hija.getWidth() / 2;
        int alto = (panel.getHeight() / 2) - hija.getWidth() / 2;
        if (hija.isShowing()) {// si se muestra
            hija.setLocation(ancho, alto);//la centramos
        } else {
            panel.add(hija);
            hija.setLocation(ancho, alto + 200);

            hija.show();//mostramos
        }
    }

    void mostrardatos(String valor) {
        Conectar1 cc = new Conectar1();
        Connection cn = cc.conexion(user, pwd);
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("caducidad");
        modelo.addColumn("costo");
        modelo.addColumn("familia");
        modelo.addColumn("proovedor");
        modelo.addColumn("precio");
        modelo.addColumn("cantidad");

        jTable1.setModel(modelo);
        String sql = "";
        if (valor.equals("")) {
            sql = "SELECT * FROM productos";
        } else {
            sql = "SELECT * FROM productos WHERE (codigo='" + valor + "')";
        }

        String[] datos = new String[8];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);

                modelo.addRow(datos);
            }
            jTable1.setModel(modelo);
        } catch (SQLException ex) {
            // Logger.getLogger(datos.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null, "ALGO SALIO MAL");

        }
    }

